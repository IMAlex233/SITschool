package com.example.luhongcheng;

public class Box {

    private String name;

    private int imageId;

    public Box(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }

    public String getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}
