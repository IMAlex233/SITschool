package com.example.luhongcheng.bean;

public class Box {

    private int name;

    private int imageId;

    public Box(int name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }



    public int getName() {
        return name;
    }

    public int getImageId() {
        return imageId;
    }

}
